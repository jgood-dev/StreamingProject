// merging all API results into one

function mergeData(jsonResultsArr) {
  let jsonArr = [];

  jsonResultsArr.forEach((serviceResult) => {
    serviceResult.results.forEach((jsonObj) => {
      jsonArr.push(jsonObj);
    });
  });

  mergedData = [...jsonArr];
  displayData(mergedData.sort(SortByAsc('imdbRating')));
}
mergeData(dataList);

function SortAsc(prop) {
  displayData(mergedData.sort(SortByAsc(prop)));
}

function SortDesc(prop) {
  displayData(mergedData.sort(SortByDesc(prop)));
}

function SortByAsc(prop) {
  return function (a, b) {
    if (a[prop] < b[prop]) {
      return 1;
    } else if (a[prop] > b[prop]) {
      return -1;
    }
    return 0;
  };
}

function SortByDesc(prop) {
  return function (a, b) {
    if (a[prop] > b[prop]) {
      return 1;
    } else if (a[prop] < b[prop]) {
      return -1;
    }
    return 0;
  };
}
